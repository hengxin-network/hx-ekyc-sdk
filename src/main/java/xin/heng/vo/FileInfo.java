package xin.heng.vo;

public class FileInfo {

    private int dp;
    private String rh;
    private String h;
    private long s;

    public int getDp() {
        return dp;
    }

    public void setDp(int dp) {
        this.dp = dp;
    }

    public String getRh() {
        return rh;
    }

    public void setRh(String rh) {
        this.rh = rh;
    }

    public String getH() {
        return h;
    }

    public void setH(String h) {
        this.h = h;
    }

    public long getS() {
        return s;
    }

    public void setS(long s) {
        this.s = s;
    }
}
